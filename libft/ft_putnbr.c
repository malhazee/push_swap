#include "libft.h"

int	ft_putnbr(int n)
{
	int	i;

	i = 0;
	if (n < 0)
	{
		write(1, "-", 1);
		i++;
		if (n == -2147483648)
		{
			write(1, "2", 1);
			i++;
			n = 147483648;
		}
		else
			n = -n;
	}
	if (n / 10)
		i += ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
	i++;
	return (i);
}
